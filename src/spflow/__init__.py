def __getattr__(name: str) -> Any:
    """互換flat APIを責務packageが必要になった時点で解決する。

    Args:
        name: `spflow`から参照された公開名。

    Returns:
        beamformingまたはfilterbank packageが公開するクラス、関数、定数。

    Raises:
        AttributeError: 遅延公開APIに登録されていない名前を参照した場合。

    Notes:
        Flow、FrameBufferなどの逐次処理部品は通常importで即時公開する。
        遅延解決は重い責務packageの依存を局所化するためだけに用いる。
    """
    if name in _BEAMFORMING_EXPORTS:
        module = import_module(".beamforming", __name__)
    elif name in _FILTERBANK_EXPORTS:
        module = import_module(".filterbank", __name__)
    else:
        raise AttributeError(f"module {__name__!r} has no attribute {name!r}")
    value = getattr(module, name)
    globals()[name] = value
    return value


def __dir__() -> list[str]:
    """通常のmodule属性と互換flat API名を列挙する。"""
    return sorted(set(globals()) | _BEAMFORMING_EXPORTS | _FILTERBANK_EXPORTS)


